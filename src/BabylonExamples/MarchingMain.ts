    getSingleMeshVertex(mesh: Mesh, meshindex: number, vNumber: number, edgeVertexIndex: number[][], voxelVertex: Vector3[]) {
        /////計算vertex 點資訊
        const edgeToCal = meshindex * 3 + vNumber;
        const verTOInterpolate: number[] = [edgeVertexIndex[edgeToCal][0], edgeVertexIndex[edgeToCal][1]]

        const pt1 = voxelVertex[verTOInterpolate[0]]
        const pt2 = voxelVertex[verTOInterpolate[1]]
        const triVertex = pt1.add(pt2).normalizeFromLength(2)

        mesh.pushVertex(triVertex);
    }

    //取得 Mesh 個數以及在index of edge
    getCountOfMesh(vox10: number): number {
        let index = 0
        for (let i = 0; i < 16; i++) {
            this.edgeIndex[i] = this.triTable[vox10][i]
            if (this.triTable[vox10][i] == -1) {
                index = i
                this.edgeIndex[i] = 0;
                break
            }
        }
        return index / 3
    }
